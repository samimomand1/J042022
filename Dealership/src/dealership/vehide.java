package dealership;

public class vehide {
   public String tires;
   public String engine;
   public String color;
   public int year;
   
 

   public vehide(String tires, String engine, String color, int year, String make, String model, String light,
		String accessories, boolean electric) {
	super();
	this.tires = tires;
	this.engine = engine;
	this.color = color;
	this.year = year;
	this.make = make;
	this.model = model;
	this.light = light;
	this.accessories = accessories;
	this.electric = electric;
}
public String make;
   public String model;
   public String light;
   public String accessories;
   public boolean electric;
   
}